  const [isLoading, setIsLoading] = useState(false);

  const login = async (phoneNumber: string, otp: string) => {
    setIsLoading(true);
    // TODO: Implement login logic
    setIsLoading(false);
  };

  const logout = () => {
    setUser(null);
  };

